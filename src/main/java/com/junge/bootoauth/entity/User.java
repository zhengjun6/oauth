package com.junge.bootoauth.entity;

public class User {
private String name;
private String pd;
private int id;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getPd() {
	return pd;
}
public void setPd(String pd) {
	this.pd = pd;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
}
